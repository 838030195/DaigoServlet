package com.Util;

public class VerificationInfo {
	
	private String userId;
	
	private String realName;
	
	private String idCode;
	
	private String schCode;
	
	private String idup;
	
	private String iddown;
	
	private String schCard;
	
	

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getIdCode() {
		return idCode;
	}

	public void setIdCode(String idCode) {
		this.idCode = idCode;
	}

	public String getSchCode() {
		return schCode;
	}

	public void setSchCode(String schCode) {
		this.schCode = schCode;
	}

	public String getIdup() {
		return idup;
	}

	public void setIdup(String idup) {
		this.idup = idup;
	}

	public String getIddown() {
		return iddown;
	}

	public void setIddown(String iddown) {
		this.iddown = iddown;
	}

	public String getSchCard() {
		return schCard;
	}

	public void setSchCard(String schCard) {
		this.schCard = schCard;
	}

	
}
